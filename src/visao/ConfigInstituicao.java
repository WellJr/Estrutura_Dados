     public void buscaDados(String nome)
    {
        
        
        int indice;
     try {
             FileReader ler = new FileReader(diretorio);
              XStream xstream = new XStream(new DomDriver());
              xstream.alias("instituicao", ArrayList.class);
              xstream.processAnnotations(Instituicao.class);
              
              BufferedReader input = new BufferedReader(new FileReader(diretorio));
              ArrayList<Instituicao> listaInstituicao2 = (ArrayList) xstream.fromXML(input);
              input.close();
              
              
              for(Instituicao instituicao: listaInstituicao2)
              {
                  
               
               
                  if (nome.equals(instituicao.getNome()))
                  {
                      
                        jTextFieldNome.setText(instituicao.getNome());
                        jTextFieldEndereco.setText(instituicao.getEndereco());
                        jTextFieldCidade.setText(instituicao.getCidade());
                        jComboBoxUf.setSelectedItem(instituicao.getEstado());
                        jFormattedTextFieldCep.setText(instituicao.getCEP());
                        jFormattedTextFieldCnpj.setText(instituicao.getCNPJ());
                        jTextFieldEmail.setText(instituicao.getEmail());
                        jTextFieldTelefone.setText(instituicao.getTelefone());
                      
                       
               }
                      
            }
              
              
         
         } catch (FileNotFoundException ex) {
             Logger.getLogger(ListaProfessores.class.getName()).log(Level.SEVERE, null, ex);
         } catch (IOException ex) {
         
             
             Logger.getLogger(CadProfessor.class.getName()).log(Level.SEVERE, null, ex);
     }
        
        
        
        
        
    }
    
    
    
    
    
    
    
    
    
    
